"""
CATERYA Export Engine — PDF/HTML/Markdown
Author : Ary HH <cateryatech@proton.me>

PDF root-cause: ReportLab built-in fonts only support Latin-1.
Emoji chars cause "An error was encountered while processing the file."
Fix: replace all emoji with ASCII before PDF rendering.
"""
from __future__ import annotations
import io, re
from datetime import datetime
from typing import Any, Dict

_EMOJI_MAP = {
    "\U0001f50d":"[Research]","\U0001f9e0":"[Analysis]","\u270d\ufe0f":"[Writer]",
    "\U0001f4e3":"[Marketing]","\U0001f4bc":"[Sales]","\U0001f4b0":"[Finance]",
    "\U0001f3d7\ufe0f":"[Architect]","\u2699\ufe0f":"[Backend]","\U0001f3a8":"[Frontend]",
    "\u2696\ufe0f":"[Ethics]","\u2705":"[PASS]","\u274c":"[FAIL]","\u26a0\ufe0f":"[WARN]",
    "\U0001f4c4":"[Doc]","\U0001f4ca":"[Chart]","\U0001f52c":"[Eval]",
    "\u2b21":"[CATERYA]","\u2192":"->","\u00b7":".","\u2026":"...",
    "\u2019":"'","\u2018":"'","\u201c":'"',"\u201d":'"',"\u2013":"-","\u2014":"--",
}

def _pdf_safe(text: str) -> str:
    if not text: return ""
    for emoji, rep in _EMOJI_MAP.items():
        text = text.replace(emoji, rep)
    return text.encode("latin-1", errors="ignore").decode("latin-1")

def _now() -> str:
    return datetime.utcnow().strftime("%Y-%m-%d %H:%M UTC")

def _safe(t):  return (t or "").strip()
def _shtml(t): return re.sub(r"<[^>]+>", "", t or "")


def to_markdown(result: Dict[str, Any], mode: str = "analyse", query: str = "") -> str:
    cos = result.get("cos_result", {})
    if not isinstance(cos, dict): cos = {}
    cs  = cos.get("cos", 0.0)
    psd = "PASS" if cs >= 0.7 else "FAIL"
    lines = [
        "# CATERYA Enterprise Report","",
        f"**Query:** {query}",
        f"**Mode:** {mode.upper()}",
        f"**Generated:** {_now()}",
        f"**COS Score:** `{cs:.4f}` -- {psd}","","---","",
    ]
    if mode == "build":
        secs = [
            ("## Architecture Design", result.get("architect_output","")),
            ("## Backend Code",        result.get("backend_code","")),
            ("## Frontend Code",       result.get("frontend_code","")),
        ]
    else:
        secs = [
            ("## Research",             result.get("research_output","")),
            ("## Analysis",             result.get("analysis_output","")),
            ("## Synthesis",            result.get("final_output","")),
            ("## Marketing Strategy",   result.get("marketing_output","")),
            ("## Sales Strategy",       result.get("sales_output","")),
            ("## Financial Projection", result.get("finance_output","")),
        ]
    for h,c in secs:
        if c and c.strip(): lines += [h,"",_safe(c),"","---",""]
    pillars = cos.get("pillars",[])
    if pillars:
        lines += ["## Ethical Evaluation","",f"**COS:** {cs:.4f} -- {psd}","",
                  "| Pillar | Score | Status |","|---|---|---|"]
        for p in pillars:
            s=p.get("score",0); st="PASS" if s>=0.7 else ("WARN" if s>=0.5 else "FAIL")
            lines.append(f"| {p.get('name','').replace('_',' ').title()} | {s:.3f} | {st} |")
    lines.append("\n*Generated by CATERYA Enterprise*")
    return "\n".join(lines)


