def to_pdf(result: Dict[str, Any], mode: str = "analyse", query: str = "") -> bytes:
    """
    Generate PDF. All text passes through _pdf_safe() to replace emoji/Unicode
    before hitting ReportLab's Latin-1 fonts.
    """
    try:
        from reportlab.lib.pagesizes import A4
        from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
        from reportlab.lib.units import cm
        from reportlab.lib import colors
        from reportlab.platypus import (
            SimpleDocTemplate, Paragraph, Spacer,
            HRFlowable, Table, TableStyle, Preformatted
        )
        from reportlab.lib.enums import TA_CENTER

        buf = io.BytesIO()
        doc = SimpleDocTemplate(buf, pagesize=A4,
            leftMargin=2.2*cm, rightMargin=2.2*cm,
            topMargin=2.2*cm, bottomMargin=2.2*cm,
            title=_pdf_safe(f"CATERYA Report - {query[:60]}"),
            author="CATERYA Enterprise")
        styles = getSampleStyleSheet()

        CA=colors.HexColor("#6c63ff"); CP=colors.HexColor("#00c99a")
        CF=colors.HexColor("#ff5555"); CW=colors.HexColor("#ffaa00")
        CD=colors.HexColor("#1a1a30"); CB=colors.HexColor("#0f0f22")
        CG=colors.HexColor("#8080b0"); CT=colors.HexColor("#c8c8e0")
        CWH=colors.HexColor("#e8e8f8")

        def ps(name,**kw):
            base=kw.pop("parent",styles["Normal"])
            return ParagraphStyle(name,parent=base,**kw)

        ST=ps("T",fontName="Helvetica-Bold",fontSize=20,textColor=CA,spaceAfter=4,leading=26)
        SH=ps("H",fontName="Helvetica-Bold",fontSize=13,textColor=CWH,spaceBefore=16,spaceAfter=5,leading=18)
        SH2=ps("H2",fontName="Helvetica-Bold",fontSize=10,textColor=CG,spaceBefore=8,spaceAfter=3)
        SB=ps("B",fontName="Helvetica",fontSize=10,textColor=CT,leading=16,spaceAfter=4)
        SM=ps("M",fontName="Courier",fontSize=9,textColor=CG)
        SC=ps("C",fontName="Courier",fontSize=8,textColor=colors.HexColor("#a0d0ff"),
              backColor=colors.HexColor("#08080f"),leading=11,leftIndent=10,rightIndent=10,
              spaceBefore=2,spaceAfter=5)
        SF=ps("F",fontName="Courier",fontSize=8,textColor=CG,alignment=TA_CENTER)

        cos=result.get("cos_result",{})
        if not isinstance(cos,dict): cos={}
        cs=cos.get("cos",0.0); psd="PASS" if cs>=0.7 else "FAIL"
        cc=CP if cs>=0.7 else CF

        story=[]
        story.append(Paragraph("CATERYA Enterprise",ST))
        story.append(Paragraph("AI-Powered Business Intelligence",SM))
        story.append(Spacer(1,8))

        mdata=[["Query",_pdf_safe(query[:120])],["Mode",mode.upper()],["Generated",_now()]]
        mt=Table(mdata,colWidths=[3*cm,14*cm])
        mt.setStyle(TableStyle([
            ("FONTNAME",(0,0),(0,-1),"Helvetica-Bold"),("FONTNAME",(1,0),(1,-1),"Helvetica"),
            ("FONTSIZE",(0,0),(-1,-1),9),("TEXTCOLOR",(0,0),(0,-1),CG),("TEXTCOLOR",(1,0),(1,-1),CT),
            ("TOPPADDING",(0,0),(-1,-1),4),("BOTTOMPADDING",(0,0),(-1,-1),4),
            ("LINEBELOW",(0,-1),(-1,-1),0.5,CD),
        ]))
        story.append(mt)
        story.append(Spacer(1,10))

        ctdata=[["COS Score",f"{cs:.4f}",psd]]
        ct=Table(ctdata,colWidths=[4*cm,3.5*cm,3.5*cm])
        ct.setStyle(TableStyle([
            ("BACKGROUND",(0,0),(-1,-1),CB),("FONTNAME",(0,0),(-1,-1),"Courier-Bold"),
            ("FONTSIZE",(0,0),(-1,-1),11),("TEXTCOLOR",(0,0),(0,0),CG),
            ("TEXTCOLOR",(1,0),(1,0),CA),("TEXTCOLOR",(2,0),(2,0),cc),
            ("BOX",(0,0),(-1,-1),1.5,CA),
            ("TOPPADDING",(0,0),(-1,-1),10),("BOTTOMPADDING",(0,0),(-1,-1),10),
            ("LEFTPADDING",(0,0),(-1,-1),14),
        ]))
        story.append(ct)
        story.append(Spacer(1,14))
        story.append(HRFlowable(width="100%",thickness=1,color=CD))
        story.append(Spacer(1,6))

        if mode=="build":
            secs=[("Architecture Design",result.get("architect_output","")),
                  ("Backend Code",result.get("backend_code","")),
                  ("Frontend Code",result.get("frontend_code",""))]
        else:
            secs=[("Research",result.get("research_output","")),
                  ("Analysis",result.get("analysis_output","")),
                  ("Final Synthesis",result.get("final_output","")),
                  ("Marketing Strategy",result.get("marketing_output","")),
                  ("Sales Strategy",result.get("sales_output","")),
                  ("Financial Projection",result.get("finance_output",""))]

        for heading,content in secs:
            if not content or not content.strip(): continue
            story.append(Paragraph(_pdf_safe(heading),SH))
            in_code=False; code_buf=[]; para_buf=[]
            for raw in content.splitlines():
                line=_pdf_safe(raw); stripped=line.strip()
                if stripped.startswith("```"):
                    if in_code:
                        ct2="\n".join(code_buf)
                        if ct2.strip(): story.append(Preformatted(ct2,SC))
                        code_buf=[]; in_code=False
                    else:
                        if para_buf:
                            story.append(Paragraph(" ".join(para_buf),SB)); para_buf=[]
                        in_code=True
                    continue
                if in_code: code_buf.append(line); continue
                if stripped.startswith("### "):
                    if para_buf: story.append(Paragraph(" ".join(para_buf),SB)); para_buf=[]
                    story.append(Paragraph(stripped[4:],SH2))
                elif stripped.startswith("## "):
                    if para_buf: story.append(Paragraph(" ".join(para_buf),SB)); para_buf=[]
                    story.append(Paragraph(stripped[3:],SH))
                elif stripped in ("---","___"):
                    if para_buf: story.append(Paragraph(" ".join(para_buf),SB)); para_buf=[]
                    story.append(HRFlowable(width="100%",thickness=0.5,color=CD))
                elif stripped=="":
                    if para_buf: story.append(Paragraph(" ".join(para_buf),SB)); para_buf=[]
                else:
                    xml=stripped.replace("&","&amp;").replace("<","&lt;").replace(">","&gt;")
                    para_buf.append(xml)
            if code_buf: story.append(Preformatted("\n".join(code_buf),SC))
            if para_buf: story.append(Paragraph(" ".join(para_buf),SB))
            story.append(Spacer(1,6))

        pillars=cos.get("pillars",[])
        if pillars:
            story.append(HRFlowable(width="100%",thickness=1,color=CD))
            story.append(Spacer(1,6))
            story.append(Paragraph("Ethical Evaluation (COS)",SH))
            pdata=[["Pillar","Score","Status"]]
            for p in pillars:
                s=p.get("score",0); st="PASS" if s>=0.7 else ("WARN" if s>=0.5 else "FAIL")
                pdata.append([p.get("name","").replace("_"," ").title(),f"{s:.3f}",st])
            pt=Table(pdata,colWidths=[6*cm,3*cm,3*cm])
            pt.setStyle(TableStyle([
                ("BACKGROUND",(0,0),(-1,0),CD),("TEXTCOLOR",(0,0),(-1,0),CG),
                ("FONTNAME",(0,0),(-1,0),"Helvetica-Bold"),("FONTNAME",(0,1),(-1,-1),"Courier"),
                ("FONTSIZE",(0,0),(-1,-1),9),
                ("ROWBACKGROUNDS",(0,1),(-1,-1),[CB,colors.HexColor("#101022")]),
                ("BOX",(0,0),(-1,-1),1,CD),("GRID",(0,0),(-1,-1),0.5,CD),
                ("TOPPADDING",(0,0),(-1,-1),7),("BOTTOMPADDING",(0,0),(-1,-1),7),
                ("LEFTPADDING",(0,0),(-1,-1),10),
            ]))
            for ri,p in enumerate(pillars,1):
                s=p.get("score",0); rc=CP if s>=0.7 else (CW if s>=0.5 else CF)
                pt.setStyle(TableStyle([("TEXTCOLOR",(1,ri),(2,ri),rc)]))
            story.append(pt)

        story.append(Spacer(1,28))
        story.append(Paragraph(
            "Generated by CATERYA Enterprise  |  cateryatech@proton.me  |  Apache 2.0",SF))

        doc.build(story)
        return buf.getvalue()

    except Exception as exc:
        import traceback as _tb
        fallback=f"PDF Error: {exc}\n\n{_tb.format_exc()}\n\n"+to_markdown(result,mode,query)
        return fallback.encode("utf-8")
